package local.onlits.onlits2001.despat;

public class MathematicalOperations {
    int add(int a, int b)
    {
        return a + b;
    }
    int sub(int a, int b)
    {
        return a +- b;
    }
    int mul(int a, int b)
    {
        return a * b;
    }
    int div(int a, int b)
    {
        return a / b;
    }
}
