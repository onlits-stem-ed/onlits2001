package local.onlits.onlits2001.basic;

public class DynamicTyping {
    public static void main(String[] args) {
        var a = 10;
        var str = "Bihar";
        a++;
        System.out.println("The value of a is " + a);
        System.out.println("The length of " + str + " is " + str.length() + " characters.");
    }
}
